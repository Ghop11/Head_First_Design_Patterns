public class GarageDoor {

    public void up(){
        System.out.print("Garage door opening up.\n");
    }
    public void down(){
        System.out.print("Garage door closing door.\n");
    }
    public void stop(){
        System.out.print("Garage door stopped moving.\n");
    }
    public void lightOn(){
        System.out.print("Garage light is on.\n");
    }
    public void lightOff(){
        System.out.print("Garage light is off.\n");
    }




}
